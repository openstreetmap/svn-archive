                <h1>Community</h1>
                <p>
					OpenStreetMap (OSM) is very much influenced by its community. Without the many volunteers who do the work, OpenStreetMap would be unthinkable. Obviously you can also "just" collect spatial data but together makes it much more fun.
                </p>

                <h2><a id="wiki" name="wiki">Wiki</a></h2>
                <p>
					The OSM wiki contains a wealth of information but it it not easily found. The wiki can be used by anyone however you need a login to contribute.  
					Links:
						<ul>
							<li><a href="http://wiki.openstreetmap.org/index.php/Main_Page">Main Wiki Page</a></li>
							<li><a href="http://wiki.openstreetmap.org/index.php/South_Africa">South African Page</a></li>
						</ul>
                </p>
               <h2><a id="ml" name="ml">Mailing Lists</a></h2>
                <p>
                    OSM operates several mailing lists. They are quite active and a good way to get started and meet the people.
				</p>
				<p>
					The <a href="http://lists.openstreetmap.org/cgi-bin/mailman/listinfo/talk-za">South African mailing list</a> is a good first stop.  There are many others especially for developers and legal issues.  The wiki has a good <a href="http://wiki.openstreetmap.org/index.php/Mailing_list">list page</a> for these.
                </p>
                <h2><a id="foren" name="foren">Forums</a></h2>
                <p>
                    OSM offers several web forums at <a href="http://forum.openstreetmap.org">forum.openstreetmap.org</a>.
                <h2><a id="irc" name="irc">IRC</a></h2>
                <p>
                    On the IRC channel #osm-za (Server: <a href="irc://irc.furion.org">irc.furion.org</a>) you will find some members of the OSM South African community. If you do not IRC client, you can also the <a href="http://www.furion.org/chat/">web interface</a>.
                </p>
                <p>
                    On the IRC channel #osm (Server: <a href="irc://irc.oftc.net">irc.oftc.net</a>) you will find some members of the OSM community. If you do not IRC client, you can also the <a href="http://irc.openstreetmap.org/">web interface</a>.
                </p>
              
                <h2><a id="party" name="party">Mapping Parties</a></h2>
                <p>
					The community meets every now and again on so-called Mapping Parties.  This will be announced and more information will be found on the South African wiki page and on the <a href="/community/#ml">mailing list</a>.

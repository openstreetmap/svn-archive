                <h2>Zum Runterladen</h2>

                <p>Bei OpenStreetmap bekommen Sie nicht nur Karten, sondern können auch die
                Rohdaten direkt herunterladen. Verschiedene Leute stellen auch Ausschnitte aus diesen
                Daten im OSM XML-Format oder in anderen Formaten zur Verfügung.</p>

                <table class="gallery">
                    <tr>
